#include <iostream>
#include <windows.h>
#include <string.h>

using namespace std;

//=============================================================================
//                           MAIN
//=============================================================================
// neuer Kommentar

int main(int, char **)
{
    SetConsoleOutputCP(CP_UTF8);

    string z = "\U000003c0";
    cout << z << endl;
    for (int i = 0; i < 10; i++)
    {
        cout << (short)z[i] << " ";
    }
    cout << endl;

    z = "\U00000300";

    cout << z << endl;
    for (int i = 0; i < 10; i++)
    {
        cout << hex << (unsigned short)z[i] << " ";
    }

    cout << endl;
    for (unsigned char i = 0; i < 0xfff; i++)
    {
        z[1] ++ ;
        if (z[i]>255)
        {
            z[i]=0;
            z[0]++;
        }
        cout << hex << (unsigned short)((z[0] << 2) | z[1]) << ": " << z << "  ";
        if (i % 16 == 0)
            cout << endl;
        
    }

    return 0;
}
